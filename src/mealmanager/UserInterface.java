    private void cmd_mailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_mailActionPerformed
        try {
            String rname = txt_p_name.getText();
            String rmonth = new DateAndTime().monthName(Login.monthAdminID);
            String rdeposite = txt_p_deposite.getText();
            String rmeal = txt_p_meal.getText();
            String rbalance = txt_p_balance.getText();
            String email = txt_p_email.getText();
            String senderEmail = "";
            String senderPass = "";

            if (!email.equals("")) {
                String msg = "Dear " + rname + "," + "\n" + "Your Report update for the month " + rmonth + " " + Login.yearAdminID + " is " + "\n"
                        + "Deposite: " + rdeposite + " TK" + "\n"
                        + "Meal: " + rmeal + "\n"
                        + "Balance: " + rbalance + " TK" + "\n"
                        + "Thanks";

                String sql = "SELECT email, emailpassword FROM month_data WHERE AdminID=" + adminId;
                preparedstatementUI = connRefUI.prepareStatement(sql);
                resultsetUI = preparedstatementUI.executeQuery();
                if (resultsetUI.next()) {
                    senderEmail = resultsetUI.getString("email");
                    senderPass = resultsetUI.getString("emailpassword");
                }

                if (senderEmail.equals("") || senderPass.equals("")) {
                    JOptionPane.showMessageDialog(null, "No Email Settings found");
                } else {
                    String sub = "Meal Account Update " + rmonth + " " + Login.yearAdminID;
                    EmailHandler updateMail = new EmailHandler();

                    Properties prop = updateMail.configProperty(senderEmail, senderPass);
                    updateMail.sendEmail(prop, email, sub, msg, null);
                    JOptionPane.showMessageDialog(null, "Update Sent!");
                }
            } else {
                JOptionPane.showMessageDialog(null, "member Email address not found!");
            }
        } catch (SQLException | MessagingException | IOException ex) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (preparedstatementUI != null) {
                    preparedstatementUI.close();
                }
                if (resultsetUI != null) {
                    resultsetUI.close();
                }
            } catch (SQLException e) {
            }
        }
    }//GEN-LAST:event_cmd_mailActionPerformed

