    private void table_Bazar_cmd_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_table_Bazar_cmd_saveActionPerformed
        try {
            String chosedDate = combo_bazar_date.getSelectedItem().toString();
            String name = combo_bazar_name.getSelectedItem().toString();
            String description = txt_bazarDescription.getText();
            String cost = txt_bazarCost.getText();

            if (chosedDate.equals("chose date") || name.equals("...") || cost == null) {
                JOptionPane.showMessageDialog(null, "One or more fields are empty");
            } else {
                time = new DateAndTime().currentTime();

                //inserting data into bazar_history table
                String sql = "UPDATE bazar_history" + Login.monthyear + " SET Time='" + time
                        + "', Name='" + name + "', Description='"
                        + description + "', Cost=" + cost + " WHERE Date='" + chosedDate + "'";
                preparedstatementUI = connRefUI.prepareStatement(sql);
                preparedstatementUI.execute();

                JOptionPane.showMessageDialog(null, "saved");

                UpdateData upd = new UpdateData(connRefUI, monthyearTagUI);
                upd.BazarTable();
                upd.report();
            }

        } catch (HeadlessException | SQLException e) {
            Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            try {
                if (preparedstatementUI != null) {
                    preparedstatementUI.close();
                }
            } catch (SQLException e) {
            }
        }

    }//GEN-LAST:event_table_Bazar_cmd_saveActionPerformed

